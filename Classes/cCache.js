//----------------------------------------------------------------------------------------------------
//  oo object.
//----------------------------------------------------------------------------------------------------
function cCache()
{
  var vCache = {};
}

//----------------------------------------------------------------------------------------------------
//  add.
//----------------------------------------------------------------------------------------------------
cCache.prototype.fSet(
  vKey,
  vData
)
{ 
  vCache[vKey] = vData;
}

//----------------------------------------------------------------------------------------------------
//  get.
//----------------------------------------------------------------------------------------------------
cCache.prototype.fGet(
  vKey
)
{
  return vCache[vKey];
}

//----------------------------------------------------------------------------------------------------
//  remove.
//----------------------------------------------------------------------------------------------------
cCache.prototype.fRemove(
  vKey
)
{
  delete vCache[vKey];
}






















